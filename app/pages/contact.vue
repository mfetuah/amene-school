useSeoMeta({
  title: 'Contact Us — AMENE International School',
  description: 'Contact Amene International School at New Abirem, Eastern Region, Ghana. Call 055 156 9990, email ameneedu@gmail.com or visit us.',
})

const submitted = ref(false)
const submitting = ref(false)
const error = ref('')
const form = reactive({ name: '', phone: '', email: '', subject: '', message: '' })

const subjects = [
  'Admissions Enquiry',
  'Fee Information',
  'Bus / Transportation',
  'Academic Matters',
  'General Enquiry',
]

async function submit() {
  submitting.value = true
  error.value = ''
  try {
    const res = await fetch('https://formspree.io/f/xzdepjde', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json', 'Accept': 'application/json' },
      body: JSON.stringify({
        name: form.name,
        phone: form.phone,
        email: form.email,
        enquiry_type: form.subject,
        message: form.message,
      }),
    })
    if (res.ok) {
      submitted.value = true
      Object.assign(form, { name: '', phone: '', email: '', subject: '', message: '' })
    } else {
      error.value = 'Something went wrong. Please try again.'
    }
  } catch {
    error.value = 'Network error. Please check your connection.'
  } finally {
    submitting.value = false
  }
}
</script>

